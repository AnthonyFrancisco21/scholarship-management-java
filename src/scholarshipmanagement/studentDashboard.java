    private void loadStatusPanel() {
            String sql = 
                "SELECT " +
                "  CONCAT(s.first_name,' ',s.middle_name,' ',s.last_name) AS full_name, " +
                "  s.email, " +
                "  sub.date_submitted, " +
                "  st.status_name " +
                "FROM student s " +
                "LEFT JOIN submission sub ON s.sub_id = sub.sub_id " +  // fixed here
                "LEFT JOIN status    st  ON s.status_id = st.status_id " +
                "WHERE s.student_id = ?";

            try (PreparedStatement pst = conn.prepareStatement(sql)) {
                pst.setInt(1, this.studentId);
                ResultSet rs = pst.executeQuery();

                if (rs.next()) {
                    
                    System.out.println(rs.getString("status_name"));
                    
                    name_status_field.setText( rs.getString("full_name") );
                    email_status_field.setText( rs.getString("email") );

                    java.sql.Date dt = rs.getDate("date_submitted");
                    date_status_field.setText(dt == null ? "â€”" : dt.toString());
                    status_field.setText( rs.getString("status_name") );
                    
                }
            } catch (SQLException ex) {
                    JOptionPane.showMessageDialog(this, 
                        "Could not load status panel:\n" + ex.getMessage(),
                        "Error", JOptionPane.ERROR_MESSAGE);
                }
        }
    
    public void announcementTable() {
        DefaultTableModel model = (DefaultTableModel) announcement_table.getModel();
        model.setRowCount(0); // Clear existing rows

        String query = "SELECT title, description, announcement_date FROM announcement_tbl";

        try (Connection conn = adminDash.DBConnection.connect();
             PreparedStatement pst = conn.prepareStatement(query);
             ResultSet rs = pst.executeQuery()) {

            while (rs.next()) {
                
                String title = rs.getString("title");
                String desc = rs.getString("description"); 
                String announcement_date = rs.getString("announcement_date");

                model.addRow(new Object[]{title, desc, announcement_date});
            }

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Error loading student submissions: " + e.getMessage());
        }

}
    
    
