    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        
        String name = admin_name_field.getText();
        String email = admin_email_field.getText();
        String password = admin_password_field.getText();

        try (Connection conn = DBConnection.connect()) {
            String updateQuery = "UPDATE admin SET name = ?, email = ?, password = ? WHERE admin_id = ?";
            PreparedStatement pst = conn.prepareStatement(updateQuery);
            pst.setString(1, name);
            pst.setString(2, email);
            pst.setString(3, password);
            pst.setInt(4, admin_id);

            int updated = pst.executeUpdate();

            if (updated > 0) {
                JOptionPane.showMessageDialog(this, "Admin updated successfully!");
                this.dispose(); // Close update form if you want
            } else {
                JOptionPane.showMessageDialog(this, "No changes were made.");
            }

            pst.close();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Error updating admin: " + e.getMessage());
        }
        
        
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

