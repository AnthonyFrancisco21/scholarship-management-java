    public updateAdd(int id) {
        initComponents();
        this.admin_id = id;
        Connection conn = new DBConnection().connect();
    
        if (conn != null) {
            System.out.println("Connection successful in update!");
            
            try {
            String query = "SELECT name, email, password FROM admin WHERE admin_id = ?";
            PreparedStatement pst = conn.prepareStatement(query);
            pst.setInt(1, admin_id);
            ResultSet rs = pst.executeQuery();

            if (rs.next()) {
                admin_name_field.setText(rs.getString("name"));
                admin_email_field.setText(rs.getString("email"));
                admin_password_field.setText(rs.getString("password"));
            }

            rs.close();
            pst.close();
            conn.close(); // Close the connection after use
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Error loading admin data: " + e.getMessage());
        }
            
            
        } else {
            System.out.println("Connection failed in update.");
        }
        
        System.out.println("Update id received! "+ admin_id);
        
        
        
    }

    
    public class DBConnection {
    private static final String URL = "jdbc:mysql://localhost:3306/bucot_db";
    private static final String USER = "root";
    private static final String PASSWORD = "";

    public static Connection connect() {
        try {
            return DriverManager.getConnection(URL, USER, PASSWORD);
        } catch (java.sql.SQLException ex) {
            System.out.println("Database connection failed in Sign Up: " + ex.getMessage());
            return null;
        }
    }
}
    
